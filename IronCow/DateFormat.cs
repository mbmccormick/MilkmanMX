
namespace IronCow
{
    public enum DateFormat
    {
        /// <summary>
        /// European date format (e.g. 14/02/06).
        /// </summary>
        European = 0,
        /// <summary>
        /// American date format (e.g. 02/14/06).
        /// </summary>
        American = 1,
        /// <summary>
        /// Default date format (American date format).
        /// </summary>
        Default = American
    }
}
